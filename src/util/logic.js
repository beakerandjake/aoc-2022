/**
 * Returns true if the left hand side is greater than the right hand side.
 */
export const greaterThan = (lhs, rhs) => lhs > rhs;

/**
 * Returns true if the left hand side is less than the right hand side.
 */
export const lessThan = (lhs, rhs) => lhs < rhs;

/**
 * Returns true if the lhs strict equals the right hand side.
 */
export const equals = (lhs, rhs) => lhs === rhs;
